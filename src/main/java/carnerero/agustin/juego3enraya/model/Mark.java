package carnerero.agustin.juego3enraya.model;

public class Mark {

	private String pathMark;
	private char mark;

	public Mark(String pathMark, char mark) {
		this.pathMark = pathMark;
		this.mark = mark;
	}

	public String getPathMark() {
		return pathMark;
	}

	public char getMark() {
		return mark;
	}

}
